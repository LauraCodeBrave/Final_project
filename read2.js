const fontPicker = document.getElementById("fontPicker");
// Apply selected font
fontPicker.addEventListener("change", function() {
  document.body.style.fontFamily = this.value;
  localStorage.setItem("selectedFont", this.value);
});

// Load previously selected font on page load
window.addEventListener("load", function() {
  const savedFont = localStorage.getItem("selectedFont");
  if (savedFont) {
    document.body.style.fontFamily = savedFont;
    fontPicker.value = savedFont;
  }
});

const books=document.querySelectorAll(".book")
const library=document.getElementById("library")
const reader=document.getElementById("reader")
const poemContainer=document.getElementById("poemContainer")
const playBtn=document.getElementById("playBtn")
const backBtn=document.getElementById("backBtn")

let currentPoem=[]

books.forEach(book=>{

book.addEventListener("click",()=>{

book.classList.add("open")

setTimeout(()=>{

library.classList.add("hidden")
reader.classList.remove("hidden")

const id=book.dataset.poem
currentPoem=poems[id]

poemContainer.innerHTML=""

currentPoem.forEach(line=>{
const p=document.createElement("p")
p.classList.add("line")
p.textContent=line
poemContainer.appendChild(p)
})

},600)

})

})

playBtn.addEventListener("click",()=>{

const lines=document.querySelectorAll(".line")

let text=""

lines.forEach(line=>{
text+=line.textContent+". "
})

const speech=new SpeechSynthesisUtterance(text)

speech.rate=0.9

let index=0

function highlight(){

if(index>0){
lines[index-1].classList.remove("highlight")
}

if(index<lines.length){
lines[index].classList.add("highlight")
index++
setTimeout(highlight,2000)
}

}

highlight()

speechSynthesis.speak(speech)

})

backBtn.addEventListener("click",()=>{

reader.classList.add("hidden")
library.classList.remove("hidden")

document.querySelectorAll(".book").forEach(book=>{
book.classList.remove("open")
})

speechSynthesis.cancel()

})

//mood tracker
document.addEventListener("DOMContentLoaded", () => {

  const picker = document.getElementById("moodPicker");
  const moods = picker.querySelectorAll("span");
  const days = document.querySelectorAll(".dayMood");
  const container = document.querySelector(".moodTracker");
  let selectedDay = null;

  // Click day to open picker
  days.forEach(day => {
    day.addEventListener("click", () => {
      selectedDay = day;
      picker.classList.remove("hidden");
    });
  });

  // Pick a mood
  moods.forEach(mood => {
    mood.addEventListener("click", () => {
      if (!selectedDay) return;

      const emoji = mood.textContent;
      const value = parseInt(mood.dataset.value); // 1-5
      const color = mood.dataset.color;
      const bgColor = mood.dataset.bg; // New: background color

      const emojiSpan = selectedDay.querySelector(".emoji");
      emojiSpan.textContent = emoji;

      // Reset all emoji sizes and glow
      days.forEach(d => {
        const e = d.querySelector(".emoji");
        e.style.transform = "scale(1)";
        e.style.textShadow = "";
      });

      // Scale emoji proportional to mood value
      let scale = 1 + (value * 0.2); // 1.2 → 2.0
      emojiSpan.style.transform = `scale(${scale})`;

      // Add glow proportional to value
      let glow = value * 4;
      emojiSpan.style.textShadow = `0 0 ${glow}px ${color}, 0 0 ${glow*1.5}px ${color}`;

      // Change mood tracker background color
      container.style.background = bgColor;

      // Hide picker
      picker.classList.add("hidden");
    });
  });

});